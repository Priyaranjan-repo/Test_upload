package com.pattern;

public class PrintStar {
	
	
	public static void main(String[] args) {
		
		
		//i =5,j=5,* print
		
		for (int i = 1; i <=5; i++) {   //print rows 5
			
			for (int j = 1; j <=5; j++) {//for star print
				System.out.print("* ");
			}
			System.out.println("");//new line
			
		}
		
		
	}

}
